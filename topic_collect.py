# Pixabay 이미지 다운로드 (config.json에 pixabay_key 있을 때만)
# ──────────────────────────────────────────────────────────────
def get_pixabay_image(query: str, save_path: Path) -> bool:
    """Pixabay에서 무료 이미지 다운로드. API 키 없으면 False 반환."""
    try:
        cfg = json.loads(CONFIG_PATH.read_text(encoding="utf-8"))
        api_key = cfg.get("pixabay_key", "")
        if not api_key:
            return False

        encoded = urllib.parse.quote(query)
        url = (
            f"https://pixabay.com/api/?key={api_key}"
            f"&q={encoded}&image_type=photo&orientation=horizontal"
            f"&per_page=5&safesearch=true&lang=ko"
        )
        with urllib.request.urlopen(url, timeout=10) as resp:
            data = json.loads(resp.read())

        hits = data.get("hits", [])
        if not hits:
            return False

        img_url = hits[0]["largeImageURL"]
        req = urllib.request.Request(
            img_url,
            headers={"User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36"}
        )
        with urllib.request.urlopen(req, timeout=15) as resp:
            save_path.write_bytes(resp.read())
        print(f"   📸 Pixabay 이미지 다운로드: {save_path.name}")
        return True

    except Exception as e:
        logging.warning(f"Pixabay 다운로드 실패: {e}")
        return False


