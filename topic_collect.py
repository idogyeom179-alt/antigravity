def main():
    """
    매일 1개 주제를 날짜 기반으로 로테이션해서 포스트 파일을 생성합니다.
    생성된 파일은 posts/ 폴더에 저장되고, blog_auto.py가 10시·17시에 자동 발행합니다.

    --all 옵션으로 실행하면 모든 주제를 한 번에 생성합니다.
    """
    import sys as _sys
    all_mode = "--all" in _sys.argv

    now       = datetime.now()
    today_str = now.strftime("%Y년 %m월 %d일")
    timestamp = now.strftime("%Y%m%d_%H%M")

    # 날짜 기반으로 오늘 주제 1개 선택 (전체 주제를 순환)
    day_index = now.timetuple().tm_yday  # 1~366
    selected_topics = TOPICS if all_mode else [TOPICS[day_index % len(TOPICS)]]

    print("=" * 52)
    print(f"  📚 주제별 포스트 생성 시작")
    print(f"  ({now.strftime('%Y-%m-%d %H:%M')})")
    if all_mode:
        print(f"  모드: 전체 {len(TOPICS)}개 주제")
    else:
        t0 = selected_topics[0]
        print(f"  오늘의 주제: {t0['emoji']} {t0['id']} (#{day_index % len(TOPICS) + 1}/{len(TOPICS)})")
    print("=" * 52)

    with sync_playwright() as p:
        browser = p.chromium.launch(headless=True)
        ctx     = browser.new_context(viewport={"width": 1280, "height": 900})
        page    = ctx.new_page()

        print("\n[1] 포스트 생성 중...")
        for t in selected_topics:
            tid = t["id"]
            print(f"\n  {t['emoji']} {tid}...")

            banner_name  = f"topic_{tid}_banner_{timestamp}.png"
            compare_name = f"topic_{tid}_compare_{timestamp}.png"
            guide_name   = f"topic_{tid}_guide_{timestamp}.png"
            tag_name     = f"topic_{tid}_tags_{timestamp}.png"
            photo_name   = None

            # SVG → PNG
            svg_to_png(page, make_topic_banner_svg(t, today_str),   BLOG_IMAGES_DIR / banner_name)
            svg_to_png(page, make_topic_comparison_svg(t),           BLOG_IMAGES_DIR / compare_name)
            svg_to_png(page, make_topic_guide_svg(t),                BLOG_IMAGES_DIR / guide_name)
            svg_to_png(page, make_topic_hashtag_svg(t),              BLOG_IMAGES_DIR / tag_name)

            # Pixabay 실사진 (API 키 있을 때만)
            photo_path = BLOG_IMAGES_DIR / f"topic_{tid}_photo_{timestamp}.jpg"
            if get_pixabay_image(t["pixabay"], photo_path):
                photo_name = photo_path.name

            # 네이버용 포스트 텍스트 생성
            content  = generate_topic_post(
                t, today_str, banner_name, compare_name,
                guide_name, photo_name, tag_name
            )
            filename = f"주제_{tid}_{timestamp}.txt"
            (POSTS_DIR / filename).write_text(content, encoding="utf-8")
            print(f"    ✅ 저장(네이버): {filename}")

            # 티스토리 전용 카드뉴스 HTML 생성
            banner_svg = (BLOG_IMAGES_DIR / banner_name.replace(".png", ".svg")).read_text(encoding="utf-8") \
                if (BLOG_IMAGES_DIR / banner_name.replace(".png", ".svg")).exists() else ""
            tistory_html = make_tistory_cardnews_html(t, today_str, banner_svg)
            ts_filename  = f"주제_{tid}_TISTORY_{timestamp}.html"
            (POSTS_DIR / ts_filename).write_text(tistory_html, encoding="utf-8")
            print(f"    ✅ 저장(티스토리): {ts_filename}")

            if photo_name:
                print(f"    📸 Pixabay 사진 포함!")
            else:
                print(f"    🎨 SVG 인포그래픽 3종 포함")

        browser.close()

    generated = len(selected_topics)
    print(f"\n총 {generated}개 포스트 파일 생성 완료! 🎉")
    print(f"→ blog_auto.py가 오전 10시·오후 5시에 자동 발행합니다.")
    print()
    logging.info(f"주제별 포스트 생성 완료 - {generated}개 ({'전체' if all_mode else '일일 1개 로테이션'})")


if __name__ == "__main__":
    main()
